    private void btThemmathangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemmathangActionPerformed

       // TODO add your handling code here:
        if(Controller.goods.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Chưa thêm mặt hàng");
            return;
        }
        MatHang mh = Controller.goods.get(cbMatHang.getSelectedIndex());
        int soluong = 0;
        try{
            soluong = Integer.parseInt(txtNhapsoluong.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "phải nhập số");
        }catch (Exception e){
            e.printStackTrace();
        }
        int count = 0;
        for(HangMua hangMua : orderGoods){
            if(hangMua.getMatHang().getMaMH() == mh.getMaMH()){
                count += hangMua.getSoluong();
            }
        }
        if(count != 0){
            for(HangMua hangMua : orderGoods){
                if(hangMua.getMatHang().getMaMH() == mh.getMaMH()){
                    hangMua.setSoluong(soluong + count);
                }
            }
        }
        else{
            HangMua hangMua = new HangMua(soluong, mh);
            orderGoods.add(hangMua);
        }
        getTonggiavaSoluong();
        showData();
        txtTongsoluong.setText(tongsoluong + "");
        txtTongtien.setText(tonggiatien + "");
    }//GEN-LAST:event_btThemmathangActionPerformed

    private void btTaodonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTaodonActionPerformed
        // TODO add your handling code here:
        if(orderGoods.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Chưa thêm mặt hàng");
            return;
        }
        Controller.setCodestaticHoaDon(Controller.getIdLastInListHoaDon(Controller.bills));
        int id = HoaDon.code;
        Date date = new Date();

        HoaDon hoaDon = new HoaDon(id, khachHang, date, orderGoods, tongsoluong, tonggiatien);
        home.addHoaDon(hoaDon);
        JOptionPane.showMessageDialog(rootPane, "Tạo đơn thành công");
        dispose();
    }//GEN-LAST:event_btTaodonActionPerformed

