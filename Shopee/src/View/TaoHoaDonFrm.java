    private void showData(){
        tblModel.setRowCount(0);
        for(HangMua hangMua : orderGoods){
            tblModel.addRow(new Object[]{
                hangMua.getMatHang().getMaMH(),
                hangMua.getMatHang().getTenMH(),
                hangMua.getMatHang().getLoaiMH(),
                hangMua.getSoluong(),
                hangMua.getTongtien()
            });
        }
    }
    private void addItemMatHang(){
        for(MatHang matHang : Controller.goods){
            cbMatHang.addItem(matHang.toString());
        }
    }
    private void getTonggiavaSoluong(){
        tonggiatien = 0;
        tongsoluong = 0;
        for(HangMua hangMua : orderGoods){
            tonggiatien += hangMua.getTongtien();
            tongsoluong += hangMua.getSoluong();
        }
    }
